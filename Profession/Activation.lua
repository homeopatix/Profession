------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Activate Plugin --
------------------------------------------------------------------------------------------
Plugins[pluginName].Load = function()
	notification("Version " .. T[ "Version" ] .. " " .. T[ "PluginName" ] .. ", " .. T[ "Loaded" ]);
end
------------------------------------------------------------------------------------------
-- Unload Plugin --
------------------------------------------------------------------------------------------
Plugins[pluginName].Unload = function()	

	SaveSettings();
	notification(T[ "Unactivated" ]);
end